package www.yunsuanfu.org;

public class yiweiyunsuanfu {
	public static void main(String[] args) {
		int i=6<<1;
		int a=6>>1;
		int b=-4>>>1;
//		使用左移和右移以及无符号位右移运算符进行运算
		System.out.println("把i向左移动一位后的结果是"+i);
		System.out.println("把a向右移动一位后的结果是"+a);
		System.out.println("把b无符号向右移动一位后的结果是"+b);
	}
}
